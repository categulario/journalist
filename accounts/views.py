def articles_journals_of_user(request, username):
    if request.user.is_authenticated:
        user = CustomUser.objects.filter(username=username).first()
        user_articles = Article_Journal.objects.filter(author_of_article=user)
        user_journals = Journal.objects.filter(journal_editor=user)

        requestable_journals = []
        if request.user.is_authenticated:
            requestable_journals = Journal.objects.exclude(journal_editor=request.user).exclude(
                requestauthorship__user=request.user, requestauthorship__status="accepted").exclude(
                requestauthorship__user=request.user, requestauthorship__status="requested").values_list("journal_name",
                                                                                                         flat=True)

        return render(request, "accounts/profile/userprofile.html",
                      context={"user": user, 'user_journals': user_journals,
                               "user_articles": user_articles, "requestable": requestable_journals})
    else:
        messages.success(request, _('Please Login or Register to View the Page!'),
                         extra_tags='danger')
        return HttpResponseRedirect(reverse('accounts:log_in'))
