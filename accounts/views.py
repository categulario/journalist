class RestorePasswordView(GuestOnlyView, FormView):
    template_name = 'accounts/restore_password.html'

    @staticmethod
    def get_form_class(**kwargs):
        if settings.RESTORE_PASSWORD_VIA_EMAIL_OR_USERNAME:
            return RestorePasswordViaEmailOrUsernameForm

        return RestorePasswordForm

    def form_valid(self, form):
        user = form.user_cache
        token = default_token_generator.make_token(user)
        uid = urlsafe_base64_encode(force_bytes(user.pk))

        if isinstance(uid, bytes):
            uid = uid.decode()

        send_reset_password_email(self.request, user.email, token, uid)

        return redirect('accounts:restore_password_done')


class ChangeProfileView(LoginRequiredMixin, FormView):
    template_name = 'accounts/profile/update_profile.html'
    form_class = ChangeProfileForm

    def get_initial(self):
        user = self.request.user

        initial = super().get_initial()

        initial['first_name'] = user.first_name
        initial['last_name'] = user.last_name
        initial['username'] = user.username
        initial['gender'] = user.userprofile.gender
        initial['profile_photo'] = user.userprofile.profile_photo
        initial['about'] = user.userprofile.about
        initial['dogum_tarihi'] = user.userprofile.dogum_tarihi

        return initial

    def form_valid(self, form):
        user = self.request.user
        user.first_name = form.cleaned_data['first_name']
        user.last_name = form.cleaned_data['last_name']
        user.username = form.cleaned_data['username']
        user.userprofile.gender = form.cleaned_data['gender']
        user.userprofile.profile_photo = form.cleaned_data['profile_photo']
        user.userprofile.about = form.cleaned_data['about']
        user.userprofile.dogum_tarihi = form.cleaned_data["dogum_tarihi"]
        user.save()
        user.userprofile.save()

        def msg():
            exp = []
            for i in form.fields:
                if form.initial[i] == form.cleaned_data.get(i, None) or form.cleaned_data.get(i, None):
                    exp.append("T")
                else:
                    exp.append("F")
            return exp

        if "F" in msg():
            messages.success(self.request, _('Your Profile Has Been Updated'))
        else:
            messages.success(self.request, _('Anything Changed :)'))

        return redirect('accounts:change_profile')


class ChangeEmailView(LoginRequiredMixin, FormView):
    template_name = 'accounts/profile/change_email.html'
    form_class = ChangeEmailForm

    def get_form_kwargs(self):
        kwargs = super().get_form_kwargs()
        kwargs['user'] = self.request.user
        return kwargs

    def get_initial(self):
        initial = super().get_initial()
        initial['email'] = self.request.user.email
        return initial

    def form_valid(self, form):
        user = self.request.user
        email = form.cleaned_data['email']

        if settings.ENABLE_ACTIVATION_AFTER_EMAIL_CHANGE:
            code = get_random_string(20)

            act = Activation()
            act.code = code
            act.user = user
            act.email = email
            act.save()

            send_activation_change_email(self.request, email, code)

            messages.success(self.request, _('Please, Click On The Link We Sent For E-Mail Exchange'))
        else:
            user.email = email
            user.save()

            messages.success(self.request, _('Email Successfully Changed'))

        return redirect('accounts:change_email')


